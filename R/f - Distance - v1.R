#################################################################################
# NAME:         FUNCTION - Distance
# AUTHOR:       Pieter Overdevest.
# DESCRIPTION:  Calculate distance between two points (x1,y1) and (x2,y2).
#################################################################################

        f_distance <- function(x1, y1, x2, y2) {

                return(sqrt((x1 - x2)^2 + (y1 - y2)^2))

        }
