        f_clean_up_header_names <- function(df.data) {
        
        
#########################################################################
# TEST ONLY
#########################################################################

        
#########################################################################
# INITIALIZATION
#########################################################################
        
        # Get and clean up header row.
        names.df.data <- names(df.data) %>%
                
                gsub("\\(|\\)|\\ |/|_|-|â€“|&|#|\\?|%|\\$|<|>|\u20AC|\\+|'|\\n|\\r|:|,",".", .) %>%
                gsub("\\.+", ".", .) %>%
                gsub("^\\.|\\.$", "", .) %>%
                tolower(.) %>%
                stringi::stri_trans_general('latin-ascii')
        
        # Clean up column names. \u20AC stands for euro sign.
        names(df.data) <- names.df.data
        
        
#########################################################################
# RETURN
#########################################################################
        
        return(df.data)
        
        }